/**
 * يولّد HTML لعلم المنتخب كصورة
 * @param {string} name - اسم المنتخب بالعربي
 * @returns {string} HTML img tag مع fallback للإيموجي
 */
export function flagImg(name) {
  const c = ISO[name];
  const em = TEAMS[name] || "";
  if (!c) return em;
  return `<img class="flg" loading="lazy" src="https://flagcdn.com/h40/${c}.png" srcset="https://flagcdn.com/h80/${c}.png 2x" alt="${em}" onerror="this.replaceWith(this.alt)">`;
}

